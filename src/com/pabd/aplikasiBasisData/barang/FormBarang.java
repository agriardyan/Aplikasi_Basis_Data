    public void isiKategori() {
        List<Kategori> daftarKategori = Kategori.lihatKategori();
        KategoriTableModel model = new KategoriTableModel(daftarKategori);

        for (int i = 0; i < daftarKategori.size(); i++) {
            kategoriBarangCB.insertItemAt(model.getValueAt(i, 0).toString() + " " + model.getValueAt(i, 1).toString(), i);
        }
    }

    private void lihatSeluruhBarang() {
        List<Barang> barangList = null;
        barangList = Barang.lihatBarang();
        BarangTableModel model = new BarangTableModel(barangList);
        tableDaftarBarang.setModel(model);
    }

    private void lihatBarang(String namaBarang) {
        List<Barang> barangList = null;
        barangList = Barang.lihatBarang(namaBarang);
        BarangTableModel model = new BarangTableModel(barangList);
        tableDaftarBarang.setModel(model);
    }

    private void lihatBarang(String kodeAwal, String kodeAkhir) {
        List<Barang> barangList = null;
        barangList = Barang.lihatBarang(kodeAwal, kodeAkhir);
        BarangTableModel model = new BarangTableModel(barangList);
        tableDaftarBarangKS.setModel(model);
    }

