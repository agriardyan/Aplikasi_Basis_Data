    private void ritelSetelahPPnTFKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ritelSetelahPPnTFKeyReleased
        // TODO add your handling code here:
        String num = ritelSetelahPPnTF.getText();

        try {
            if (num == null) {
                num = "0";
            } else {
                num = num.replace(".", "");
                num = NumberFormat.getNumberInstance(new Locale("id", "ID")).format(Integer.parseInt(num));
                num = num.replaceAll(",", ".");
            }
        } catch (NumberFormatException ex) {
            ex.getMessage();
        }

        ritelSetelahPPnTF.setText(num);
    }//GEN-LAST:event_ritelSetelahPPnTFKeyReleased

