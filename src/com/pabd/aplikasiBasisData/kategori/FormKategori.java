    private void hapusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusButtonActionPerformed
        // TODO add your handling code here:
        String kode = dataBarangTable.getValueAt(dataBarangTable.getSelectedRow(), 0).toString();
        mKategori.setmKodeKategori(kode);
        int a = JOptionPane.showConfirmDialog(this, "Anda akan menghapus record ini. Lanjutkan?", "Hapus Record", 0);
        
        if (a != 0) {
            return;
        }
        
        Kategori.deleteKategori(mKategori);
        
        lihatTableKategori();
        batalButton.doClick();

    }//GEN-LAST:event_hapusButtonActionPerformed

