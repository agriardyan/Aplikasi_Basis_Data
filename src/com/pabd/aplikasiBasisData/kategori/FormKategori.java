    private void simpanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanButtonActionPerformed
        // TODO add your handling code here:
        mKategori.setmKodeKategori(kodeTF.getText().toUpperCase());
        mKategori.setmNamaKategori(namaTF.getText().toUpperCase());

        if (simpanButton.getText() == "Update") {
            Kategori.updateKategori(temp, mKategori);

            batalButton.doClick();
            simpanButton.setText("Simpan");
            lihatTableKategori();
            return;
        }

//            status = db.insertIntoKategori(kodeTF.getText(), namaTF.getText());
        Kategori.simpanKategori(mKategori);
        lihatTableKategori();
        batalButton.doClick();

//        dataBarangTable = db.showTable();
//        JOptionPane.showMessageDialog(this, db.showTable());

    }//GEN-LAST:event_simpanButtonActionPerformed

